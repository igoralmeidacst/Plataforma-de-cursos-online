<h3 class="container"><strong>Descrição</strong></h3><br>
<p class="container" style="font-size: 17px;">
    Preparei este curso com muito carinho, com o objetivo de demonstrar todas as principais funcionalidades existentes,
    tanto em ambiente on-premisse (servidores físicos e virtuais) quanto na Nuvem (Azure SQL Databases).
    <br>
<p class="container" style="font-size: 17px; padding-top: 10px;">
    Meu objetivo foi criar um curso de totalmente gratuito frente a quantidade de horas do mesmo, para que tenhas
    condições de se tornar um DBA, em um curto espaço de tempo ou melhorar suas habilidades.</p>
<p class="container" style="font-size: 17px; padding-top: 10px;">
    Participei como aluno em muitos cursos de SQL Server ao longo da minha carreira e ministrei muitos também e uma das
    minhas reclamações e frustraçôes sempre foram a quantidade de horas perdidas em slides, conversas em paralelo e
    laboratórios feito pelos próprios alunos apenas seguindo o lab de exercício. Na verdade o que eu mais queria era as
    demonstrações do professor, direto no SQL-Server. </p>
<p class="container" style="font-size: 17px; padding-top: 10px;">
    Neste curso irá aprender, desde a configuração do seu ambiente (vm, ad, dhcp, dominio, etc) até ajustes de
    performance, segurança e auditoria, passando por MS Failover Cluster, Always ON, replicação, linked server,
    segurança, auditoria, linguagem SQL, conceitos de modelagem de dados, ferramentas para designer de modelos de dados,
    criar máquinas virtuais através da Azure, configurar blob containers e vaults para realizar backups e restores,
    criar bancos de dados como serviço na Azure (paas), seguindo as melhores práticas e minha vivencia diária como DBA e
    administrador de dados (AD) desde 1996.</p>
<p class="container" style="font-size: 17px; padding-top: 10px;">
    Não tenho a pretensão de ser o melhor professor, longe disto, mas meu objetivo foi preparar o melhor curso possivel,
    o mais completo disponivel na internet com nenhum custo, e tenho certeza que irá aprender muitas coisas
    novas e irá se valorizar como profissional, e com dedicação para absorver estas informações e prática, seguindo meus
    vídeos e scripts disponibilizados, poderá ter novas oportunidades profissionais no Brasil ou em outros países.
    organizar, estruturar e distribuir as informações de uma empresa.</p>
<p class="container" style="font-size: 17px; padding-top: 10px;">
