<h3 class="container"><strong>Descrição</strong></h3><br>
<p class="container" style="font-size: 17px;">
    C# é uma linguagem moderna Orientada a Objetos que possui uma série de recursos fantásticos e é uma das melhores
    opções para se iniciar no mundo da programação. A linguagem C# faz parte do .NET framework desenvolvido pela
    Microsoft.
    <br>
<p class="container" style="font-size: 17px; padding-top: 10px;">
    Seja um poliglota na programação! A cada nova linguagem que você aprende, novos conceitos e abordagens são
    aprendidos e isso sem dúvida te tornará um programador melhor!</p>
<p class="container" style="font-size: 17px; padding-top: 10px;">
    E esse curso tem por objetivo apresentar a linguagem C# e os seus recursos e está dividido em 12 seções, quais
    sejam:</p>

<li class="container" style="font-size: 17px; padding-top: 10px;">Introdução - Principais temas abordados são:
    algoritmos, estrutura de dados e introdução ao .NET framework;</li>
<li class="container" style="font-size: 17px;">Configuração do Ambiente - Os passos necessários para configurar o
    computador para executar o C#;</li>
<li class="container" style="font-size: 17px;">Fundamentos - Tem por objetivo estabelecer bases sólidas sobre a
    linguagem;</li>
<li class="container" style="font-size: 17px;">Estruturas de Controle - Aqui aprenderemos a controlar o fluxo da
    aplicação;</li>
<li class="container" style="font-size: 17px;">Classes & Métodos - Aqui inicia a nossa jornada pela Orientação a Objetos
    com conceitos essenciais que todo desenvolvedor precisa saber;</li>
<li class="container" style="font-size: 17px;">Coleções - Várias estruturas de dados clássicas serão abordadas: array,
    listas, filas, pilhas, dicionários e etc</li>
<li class="container" style="font-size: 17px;">Orientação a Objetos - Os pilares das Orientação a Objetos e como aplicar
    na prática;</li>
<li class="container" style="font-size: 17px;">Métodos e Funções - O C# também suporte recursos do mundo do funcional, e
    nessa seção vermos o poder das funções, lambdas e métodos;</li>
<li class="container" style="font-size: 17px;">Exceções - Quando os erros ocorrem entram em cenas as exceções e as
    técnicas de tratamento de erro;</li>
<li class="container" style="font-size: 17px;">Explorando API - Aprenderemos a mexer com arquivos, datas e intervalos de
    datas;</li>
<li class="container" style="font-size: 17px;">Tópicos Avançados - Generics, LINQ, Dynamic, Nullables e etc;</li>
<li class="container" style="font-size: 17px;">Conclusão - Despedida e agradecimento;</li>

<p class="container" style="font-size: 17px; padding-top: 10px;">
    Bons estudos!
</p>
